#include "libft.h"

char	*ft_strcpy(char *dst, char const *src)
{
	size_t	i;

	if (!dst)
		return (0);
	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
