#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	char	*p;
	size_t	i;

	p = (char*)str;
	i = 0;
	while (i < n)
	{
		p[i] = c;
		i++;
	}
	return (p);
}
